/*3. 题目：求 e 的近似值，公式e=1+1/1!+1/2!+1/3!+......，要求精度与e=2.718281828459...相比不超过0.000001。
提示：精度做循环条件，若精度不满足则继续求，例如若e’=2.7183，与e的所给值e=2.718281828459...相比精度大概为0.0001，就大于0.000001，不满足精度要求，继续求解。*/
#include <stdio.h>
int main(void)
{
    int i, t = 1;
    double e = 0;
    for (i = 1; t < 100000000; i++)
    {
        e += 1.0 / t;
        t *= i;
    }
    printf("e=%.12lf\n", e);
    return 0;
}